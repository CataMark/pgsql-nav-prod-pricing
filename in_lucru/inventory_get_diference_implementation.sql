    create temp table tbl_tmp_orders on commit drop as
    select
        a."Status",
        a."No_",
        a."Description",
        a."Location Code",
        a."Source No_"
    from nav.tbl_int_production_order as a
    where a."Status" = 3 /* released */ or
        (a."Last Date Modified"::date between '2022-03-01'::date and '2022-03-31'::date and a."Status" = 4 /* closed */);
    create unique index if not exists tbl_tmp_orders_ix1 on tbl_tmp_orders("No_");

    create temp table tbl_tmp_results (
        locatie text not null,
        reper text not null,
        stoc_impact decimal(20, 2),
        dif_inventar decimal(20, 2),
        umas text not null,
        descriere text not null,
        comanda text not null,
        comanda_linie int
    );

    /* check if item output is higher than capacity record */
    /**************************************************************/
    insert into tbl_tmp_results (locatie, reper, stoc_impact, dif_inventar, umas, descriere, comanda, comanda_linie)
    select
        a.locatie,
        a.reper,
        (coalesce(e.capacity_output, 0) - coalesce(f.item_ledger_quantity, 0)) as stoc_impact,
        a.cantitate as dif_inventar,
        'UN' as umas,
        'cantitate iesire mai mare decat declarat pe ultima operatie' as descriere,
        b."Prod_ Order No_" as comanda,
        b."Line No_" as comanda_linie
    from tbl_tmp_diferente as a

    inner join nav.tbl_int_prod_order_line as b
    on b."Item No_" = a.reper and b."Location Code" = a.locatie

    inner join tbl_tmp_orders as c
    on c."No_" = b."Prod_ Order No_"

    left join lateral(
        select
            d1."Operation No_"
        from nav.tbl_int_prod_order_routing_line as d1
        where d1."Prod_ Order No_" = b."Prod_ Order No_" and d1."Routing Reference No_" = b."Line No_"
        order by (case when nullif(d1."Next Operation No_", '') is null then 1 else 2 end) asc, d1."Operation No_" desc
        limit 1
    ) as d
    on true

    left join lateral (
        select
            sum(e1."Output Quantity") as capacity_output
        from nav.tbl_int_capacity_ledger_entry as e1
        where e1."Order No_" = b."Prod_ Order No_" and e1."Order Line No_" = b."Line No_" and e1."Operation No_" = d."Operation No_" and
            e1."Posting Date"::date between _start_date and _end_date
    ) as e
    on true

    left join lateral (
        select
            sum(f1."Quantity") as item_ledger_quantity
        from nav.tbl_int_item_ledger_entry as f1
        where f1."Order No_" = b."Prod_ Order No_" and f1."Order Line No_" = b."Line No_" and f1."Item No_" = b."Item No_" and
            f1."Entry Type" = 6 /* output */ and f1."Posting Date"::date between _start_date and _end_date
    ) as f
    on true

    where a.tip = 'minus' and coalesce(e.capacity_output, 0) != coalesce(f.item_ledger_quantity, 0); 

    /* check if consumption not done with routing link and quantity per*/
    /**************************************************************/
    insert into tbl_tmp_results (locatie, reper, stoc_impact, dif_inventar, umas, descriere, comanda, comanda_linie)
    select
        x.locatie,
        x.reper,
        (coalesce(g.item_ledger_quantity, 0) - x.capacity_consumption) as stoc_impact,
        x.dif_inventar,
        h."Base Unit of Measure" as umas,
        'consum diferit decat necesar pentru operatia aferenta legaturii circuit' as descriere,
        x.comanda,
        x.comanda_linie
    from (
        select
            a.locatie,
            a.reper,
            a.cantitate as dif_inventar,
            b."Prod_ Order No_" as comanda,
            b."Prod_ Order Line No_" as comanda_linie,
            sum(coalesce(e.capacity_output, 0) * b."Quantity per" * 
                (case when a.umas = b."Unit of Measure Code" then 1
                    else coalesce(g1."Qty_ per Unit of Measure", 1) / coalesce(g2."Qty_ per Unit of Measure", 1) end)) as capacity_consumption
        from tbl_tmp_diferente as a

        inner join nav.tbl_int_prod_order_component as b
        on b."Location Code" = a.locatie and b."Item No_" = a.reper and b."Quantity per" != 0 and nullif(b."Routing Link Code", '') is not null

        inner join tbl_tmp_orders as c
        on c."No_" = b."Prod_ Order No_"

        inner join nav.tbl_int_prod_order_routing_line as d
        on d."Prod_ Order No_" = b."Prod_ Order No_" and d."Routing Reference No_" = b."Prod_ Order Line No_" and d."Routing Link Code" = b."Routing Link Code"

        left join lateral (
            select
                e1."Operation No_",
                sum(e1."Output Quantity" + e1."Scrap Quantity") as capacity_output
            from nav.tbl_int_capacity_ledger_entry as e1
            where e1."Order No_" = d."Prod_ Order No_" and e1."Routing Reference No_" = d."Routing Reference No_" and e1."Operation No_" >= d."Operation No_" and
                e1."Posting Date"::date <= _end_date
            group by "Operation No_"
            order by capacity_output desc
            limit 1
        ) as e
        on true

        left join nav.tbl_int_item_umas as g1
        on a.reper = g1."Item No_" and a.umas = g1."Code"

        left join nav.tbl_int_item_umas as g2
        on a.reper = g2."Item No_" and b."Unit of Measure Code" = g2."Code"

        where a.tip = 'minus'

        group by a.locatie,
                a.reper,
                a.cantitate,
                b."Prod_ Order No_",
                b."Prod_ Order Line No_"
    ) as x

    left join lateral (
        select
            -1 * sum(g1."Quantity") as item_ledger_quantity
        from nav.tbl_int_item_ledger_entry as g1
        where g1."Order No_" = x.comanda and g1."Document No_" = x.comanda and g1."Order Line No_" = x.comanda_linie and g1."Item No_" = x.reper and
            g1."Entry Type" = 5 and g1."Posting Date"::date <= _end_date
    ) as g
    on true

    left join nav.tbl_int_item as h
    on x.reper = h."No_"

    where x.capacity_consumption != coalesce(g.item_ledger_quantity, 0);
end;
